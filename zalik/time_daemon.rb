# Демон, що перевіряє системний час
thread = Thread.new do
  loop do
    # Отримання поточного часу
    current_time = Time.now
    puts "Системний час: #{current_time}"

    # Очікування 1 хвилину
    sleep(60)
  end
end

# Дозволяємо потоку завершуватися разом із головною програмою
thread.abort_on_exception = true

# Основна програма
puts "Програма запущена. Демон працює у фоновому режимі..."
puts "Натисніть Ctrl+C для завершення програми."

# Запобігання завершенню головного потоку
loop do
  sleep(1)
end
